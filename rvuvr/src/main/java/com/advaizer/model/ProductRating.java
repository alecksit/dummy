package com.advaizer.model;

/**
 * @author ANKIT
 *
 */
public class ProductRating {
	int ratingId;
	int ratingPoint;
	int productId;
	int userId;
	/**
	 * @return the ratingId
	 */
	public int getRatingId() {
		return ratingId;
	}
	/**
	 * @param ratingId the ratingId to set
	 */
	public void setRatingId(final int ratingId) {
		this.ratingId = ratingId;
	}
	/**
	 * @return the ratingPoint
	 */
	public int getRatingPoint() {
		return ratingPoint;
	}
	/**
	 * @param ratingPoint the ratingPoint to set
	 */
	public void setRatingPoint(final int ratingPoint) {
		this.ratingPoint = ratingPoint;
	}
	/**
	 * @return the productId
	 */
	public int getProductId() {
		return productId;
	}
	/**
	 * @param productId the productId to set
	 */
	public void setProductId(final int productId) {
		this.productId = productId;
	}
	/**
	 * @return the userId
	 */
	public int getUserId() {
		return userId;
	}
	/**
	 * @param userId the userId to set
	 */
	public void setUserId(final int userId) {
		this.userId = userId;
	}
	

}
